// Language utilities
const LanguageManager = {
  currentLanguage: 'en',
  
  // Initialize language
  init() {
    // Get saved language preference
    const savedLang = localStorage.getItem('rapidblood_language');
    if (savedLang && languages[savedLang]) {
      this.currentLanguage = savedLang;
    } else {
      // Auto-detect Bengali
      const userLang = navigator.language || navigator.userLanguage;
      if (userLang.startsWith('bn')) {
        this.currentLanguage = 'bn';
      }
    }
    
    // Apply language immediately
    this.applyLanguage();
    
    // Set HTML lang attribute
    document.documentElement.lang = this.currentLanguage;
    
    // Log for debugging
    console.log('LanguageManager initialized with:', this.currentLanguage);
  },
  
  // Change language
  changeLanguage(lang) {
    if (languages[lang]) {
      this.currentLanguage = lang;
      localStorage.setItem('rapidblood_language', lang);
      
      // Update HTML lang attribute
      document.documentElement.lang = lang;
      
      // Apply language to page
      this.applyLanguage();
      
      // Log for debugging
      console.log('Language changed to:', lang);
      
      return true;
    }
    return false;
  },
  
  // Get translation
  t(key) {
    const lang = languages[this.currentLanguage];
    return lang && lang[key] ? lang[key] : key;
  },
  
  // Apply language to page
  applyLanguage() {
    console.log('Applying language:', this.currentLanguage);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      const translation = this.t(key);
      
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        if (element.hasAttribute('placeholder')) {
          element.setAttribute('placeholder', translation);
        }
      } else {
        element.textContent = translation;
      }
    });
    
    // Update elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
      const key = element.getAttribute('data-i18n-placeholder');
      const translation = this.t(key);
      element.setAttribute('placeholder', translation);
    });
    
    // Update page title
    const titleElement = document.querySelector('title');
    if (titleElement) {
      titleElement.textContent = this.t('rapidblood') || 'RapidBlood';
    }
    
    // Trigger custom event for components that need to update
    document.dispatchEvent(new CustomEvent('languageChanged', {
      detail: { language: this.currentLanguage }
    }));
    
    console.log('Language applied successfully');
  },
  
  // Get current language
  getCurrentLanguage() {
    return this.currentLanguage;
  },
  
  // Get available languages
  getAvailableLanguages() {
    return Object.keys(languages);
  },
  
  // Check if language is initialized
  isInitialized() {
    return this.currentLanguage !== 'en' || localStorage.getItem('rapidblood_language');
  }
};

